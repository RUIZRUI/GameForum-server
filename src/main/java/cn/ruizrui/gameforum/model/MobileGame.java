package cn.ruizrui.gameforum.model;

import java.util.Date;

public class MobileGame {
	protected String game_id;
	protected String game_name;
	protected String game_slogan;
	protected String game_version;
	protected String game_platform;
	protected String game_type;
	protected Date game_release_date;
	protected String game_release;
	protected String game_language;
	protected float game_score;
	protected int game_rater_num;
	protected String game_img;
	
	public String getGame_id() {
		return game_id;
	}
	public void setGame_id(String game_id) {
		this.game_id = game_id;
	}
	public String getGame_name() {
		return game_name;
	}
	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}
	public String getGame_slogan() {
		return game_slogan;
	}
	public void setGame_slogan(String game_slogan) {
		this.game_slogan = game_slogan;
	}
	public String getGame_version() {
		return game_version;
	}
	public void setGame_version(String game_version) {
		this.game_version = game_version;
	}
	public String getGame_platform() {
		return game_platform;
	}
	public void setGame_platform(String game_platform) {
		this.game_platform = game_platform;
	}
	public String getGame_type() {
		return game_type;
	}
	public void setGame_type(String game_type) {
		this.game_type = game_type;
	}
	public Date getGame_release_date() {
		return game_release_date;
	}
	public void setGame_release_date(Date game_release_date) {
		this.game_release_date = game_release_date;
	}
	public String getGame_release() {
		return game_release;
	}
	public void setGame_release(String game_release) {
		this.game_release = game_release;
	}
	public String getGame_language() {
		return game_language;
	}
	public void setGame_language(String game_language) {
		this.game_language = game_language;
	}
	public float getGame_score() {
		return game_score;
	}
	public void setGame_score(float game_score) {
		this.game_score = game_score;
	}
	public int getGame_rater_num() {
		return game_rater_num;
	}
	public void setGame_rater_num(int game_rater_num) {
		this.game_rater_num = game_rater_num;
	}
	public String getGame_img() {
		return game_img;
	}
	public void setGame_img(String game_img) {
		this.game_img = game_img;
	}

	
}
