package cn.ruizrui.gameforum.model;
public class HostGame {
	protected String game_id;
	protected String game_name;
	protected String game_type;
	protected String game_website;
	protected String game_label;
	protected float game_score;
	protected int game_rater_num;
	protected String game_img;
	public String getGame_id() {
		return game_id;
	}
	public void setGame_id(String game_id) {
		this.game_id = game_id;
	}
	public String getGame_name() {
		return game_name;
	}
	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}
	public String getGame_type() {
		return game_type;
	}
	public void setGame_type(String game_type) {
		this.game_type = game_type;
	}
	public String getGame_website() {
		return game_website;
	}
	public void setGame_website(String game_website) {
		this.game_website = game_website;
	}
	public String getGame_label() {
		return game_label;
	}
	public void setGame_label(String game_label) {
		this.game_label = game_label;
	}
	public float getGame_score() {
		return game_score;
	}
	public void setGame_score(float game_score) {
		this.game_score = game_score;
	}
	public int getGame_rater_num() {
		return game_rater_num;
	}
	public void setGame_rater_num(int game_rater_num) {
		this.game_rater_num = game_rater_num;
	}
	public String getGame_img() {
		return game_img;
	}
	public void setGame_img(String game_img) {
		this.game_img = game_img;
	}
	
}
